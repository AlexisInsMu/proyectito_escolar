    private void crearMateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearMateriaActionPerformed
        try {
            String materia = nombreMateria.getText();
            String grupo = perteneceMateria.getText();
            
            if (uni.buscarGrupoPorNombre(grupo) != null) {
                if (uni.buscarGrupoPorNombre(grupo).buscarMateriaPorNombre(materia) != null) {
                    JOptionPane.showMessageDialog(this, "La materia ya existe en el grupo indicado.");
                    return;
                }
            }
            
            Profesor profesorVacio = new Profesor();
            Materia nuevaMateria = new Materia(materia, profesorVacio, new Grupo());
            uni.addMateria(nuevaMateria, uni.buscarGrupoPorNombre(grupo));
            
            crearProfe profesor = new crearProfe(uni, nuevaMateria, 1); 
            profesor.crearProfesor(uni, nuevaMateria, 1); 
            profesor.setVisible(true);
            
            if (profesor == null) {
                JOptionPane.showMessageDialog(this, "No se pudo asignar un profesor a la materia.");
            } else {
                JOptionPane.showMessageDialog(this, "Materia agregada exitosamente.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurri√≥ un error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_crearMateriaActionPerformed

