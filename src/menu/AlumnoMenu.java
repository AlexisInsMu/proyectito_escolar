    private void verMateriasInscritasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verMateriasInscritasActionPerformed
        String matricula = JOptionPane.showInputDialog("Dame la matricula del alumno:");
        if (uni.buscarAlumnoPorMatricula(matricula) != null) {
            JOptionPane.showMessageDialog(this, uni.buscarAlumnoPorMatricula(matricula).imprimirMaterias());
        } else {
            JOptionPane.showMessageDialog(this, "Alumno no encontrado","Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_verMateriasInscritasActionPerformed

